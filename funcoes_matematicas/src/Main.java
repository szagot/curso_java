
public class Main {

	public static void main(String[] args) {

		double x;

		// Raiz quadrada de 9
		x = Math.sqrt(9);
		System.out.println("Raiz quadrada de 9 = " + x);

		// Potência 4³
		x = Math.pow(4, 3);
		System.out.println("4³ = " + x);

		// Valor absoluto
		x = -25.0;
		System.out.println("Valor absoluto de " + x + " é " + Math.abs(x));

	}

}
